     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if(request()->has('cari')) {
            $keyword = \Request::get('cari');
            $penghuni_daftar = penghuni::where('nama', 'like', '%'.$keyword.'%')
                ->paginate(5)
                ->appends('cari', request('cari'));
        }

        return view('pencarian', compact('penghuni_daftar'));
    }

